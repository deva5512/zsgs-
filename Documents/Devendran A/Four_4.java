package zohosums;

public class Four_4 {
	public static void main(String[] args) {
		int a = 2;
		int b = a<<1;
		System.out.println(b);
		int a1 = 2;
		int b1 = a1<<2;
		System.out.println(b1);
		System.out.println(4>>1);
		System.out.println(4>>>1);
		System.out.println(-4>>1);
		System.out.println(-4>>>1);
		
		boolean des = true;
		System.out.println(!des);
	}
}
